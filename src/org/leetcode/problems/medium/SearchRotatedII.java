    public boolean search(int[] nums, int target) {
    	//find Min
    	int minIdx = findMin(nums);
    	int low = 0;
    	int high = nums.length-1;
    	
    	if(target == nums[minIdx]) 
    		return true;
    	if(minIdx > 0){
        	low = target<=nums[nums.length-1]?minIdx:0;
        	high = target>nums[nums.length-1]?minIdx:nums.length-1;
    	} 
    	
    	while(low <= high){
    		int mid = (low+high)/2;
    		if(target==nums[mid]){
    			return true;
    		} else if(target < nums[mid]){
    			high = mid-1;
    		} else {
    			low = mid+1;
    		}
    	}
    	return false;
    }
    
	public int findMin(int[] nums){
		int low =0;
		int high = nums.length-1;
		while(low <= high){
			int mid = (low+high)/2;
			if(nums[mid]==nums[high] && nums[mid]==nums[low]){
				high--;
				low++;
			} else if(nums[mid] == nums[high] && nums[mid] < nums[low]){  
				low++;
				high--;
			}else{
				high = mid;
			}
		}
		return low;
	}
